# Keyboard Shortcuts:
# - Function + right arrow: to go to the end of the line
# - Function + left arrow: to go to the beginning of the line
# - Function + up arrow: to go to the beginning of the file
# - Function + down arrow: to go to the end of the file
# - option + up/down arrow: to move the line up or down
# - command + slash: to comment the line
