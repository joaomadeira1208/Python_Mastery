# Keyboard Shortcuts:
#   - End key: to go to the end of the line
#   - Home key: to go to the beginning of the line
#   - Ctrl + End key: to go to the end of the file
#   - Ctrl + Home key: to go to the beginning of the file
#   - alt + up/down arrow: to move the line up or down
#   - shift + alt + up/down arrow: to duplicate the line up or down
#   - Ctrl + ;: to comment the line
#   - Ctrl + D: to select the word
